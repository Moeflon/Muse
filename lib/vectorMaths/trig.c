int8_t sin100(int16_t degrees) {
	int sign = 1;
	if(degrees < 0) {
		degrees = -degrees;
		sign = -1;
	}
	if(degrees > 90 && degrees <= 180) {
		degrees = 180 - degrees;
	}
	else if(degrees > 180) {
		degrees -= 180;
		sign = -1;
	}
	return sign * sin_table[degrees];
}

int8_t cos100(int16_t degrees) {
	return sin100(90 - degrees);
}

